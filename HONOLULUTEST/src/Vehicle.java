public class Vehicle {
    String brand;
    String model;
    String gear;
    int horsePower;
    int fuelTime;
    int regNr;
    double odometer;
    public Vehicle(String b, String mo, String g, int hp, int ft, int rn, double od ){
        brand = b;
        model = mo;
        gear = g;
        horsePower = hp;
        fuelTime = ft;
        regNr = rn;
        odometer = od;

    }
}